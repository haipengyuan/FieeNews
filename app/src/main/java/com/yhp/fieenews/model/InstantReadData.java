package com.yhp.fieenews.model;



public class InstantReadData {
    String content;
    String siteName;
    String siteSlug;
    String title;
    String url;

    public String getContent() {
        return content;
    }

    public String getSiteName() {
        return siteName;
    }

    public String getSiteSlug() {
        return siteSlug;
    }

    public String getTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }
}
